    public void login() throws FileNotFoundException, IOException, TwitterException {
        handler = new TokenHandler();
        consumerKey = handler.retrieveConsumerKey();
        consumerSecret = handler.retrieveConsumerSecretKey();
        accessToken = handler.retrieveToken();
        accessTokenSecret = handler.retrieveTokenSecret();
        twitter = new TwitterFactory().getInstance();
        token = new AccessToken(accessToken, accessTokenSecret);
        twitter.setOAuthConsumer(consumerKey, consumerSecret);
        twitter.setOAuthAccessToken(token);
        screenNameText.setText(twitter.getScreenName());
        update();
    }

