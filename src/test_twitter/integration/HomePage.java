    public void update() {
        try {

            tweetArea.setText("");
            friendsArea.setText("");
            TwitterHandler twitterHandler = new TwitterHandler();

            List<Status> tweets = twitterHandler.getTweets(twitter);
            List<Status> friendsTweets = twitterHandler.getFriendsTweets(twitter);
            for (int i = 0; i < tweets.size(); i++) {
                tweetArea.append("\n" + tweets.get(i).getUser().getName() + " " + "[" + tweets.get(i).getCreatedAt() + "]" + " --->" + " " + tweets.get(i).getText() + "\n");
            }

            for (int i = 0; i < friendsTweets.size(); i++) {
                friendsArea.append("\n" + friendsTweets.get(i).getUser().getName() + " " + "[" + friendsTweets.get(i).getCreatedAt() + "]" + " --->" + " " + friendsTweets.get(i).getText() + "\n");
            }

        } catch (TwitterException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

